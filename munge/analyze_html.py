def section_footer():
	s = ''
	s += '<!-- END MAIN -->\n'
	s += '</div>\n'
	s += '\n'
	s += '<script>\n'
	s += 'filterSelection("all")\n'
	s += 'function filterSelection(c) {\n'
	s += '  var x, i;\n'
	s += '  x = document.getElementsByClassName("column");\n'
	s += '  if (c == "all") c = "";\n'
	s += '  for (i = 0; i < x.length; i++) {\n'
	s += '    w3RemoveClass(x[i], "show");\n'
	s += '    if (x[i].className.indexOf(c) > -1) w3AddClass(x[i], "show");\n'
	s += '  }\n'
	s += '}\n'
	s += '\n'
	s += 'function w3AddClass(element, name) {\n'
	s += '  var i, arr1, arr2;\n'
	s += '  arr1 = element.className.split(" ");\n'
	s += '  arr2 = name.split(" ");\n'
	s += '  for (i = 0; i < arr2.length; i++) {\n'
	s += '    if (arr1.indexOf(arr2[i]) == -1) {element.className += " " + arr2[i];}\n'
	s += '  }\n'
	s += '}\n'
	s += '\n'
	s += 'function w3RemoveClass(element, name) {\n'
	s += '  var i, arr1, arr2;\n'
	s += '  arr1 = element.className.split(" ");\n'
	s += '  arr2 = name.split(" ");\n'
	s += '  for (i = 0; i < arr2.length; i++) {\n'
	s += '    while (arr1.indexOf(arr2[i]) > -1) {\n'
	s += '      arr1.splice(arr1.indexOf(arr2[i]), 1);\n'
	s += '    }\n'
	s += '  }\n'
	s += '  element.className = arr1.join(" ");\n'
	s += '}\n'
	s += '\n'
	s += '// Add active class to the current button (highlight it)\n'
	s += 'var btnContainer = document.getElementById("myBtnContainer");\n'
	s += 'var btns = btnContainer.getElementsByClassName("btn");\n'
	s += 'for (var i = 0; i < btns.length; i++) {\n'
	s += '  btns[i].addEventListener("click", function(){\n'
	s += '    var current = document.getElementsByClassName("active");\n'
	s += '    current[0].className = current[0].className.replace(" active", "");\n'
	s += '    this.className += " active";\n'
	s += '  });\n'
	s += '}\n'
	s += '</script>\n'
	s += '\n'
	s += '</body>\n'
	s += '</html>\n'
	return s