def section_buttons(gallery):
	#print(gallery)
	topics = {}
	for i, (k, im) in enumerate(sorted(gallery.items())):
		topics[im.type.replace(' ', '_')] = im.type
	s = ''
	#print(topics)
	s += '<div id="myBtnContainer">\n'
	for i, (k, v) in enumerate(sorted(topics.items())):
		if (i == 0):
			s += '  <button class="btn active" onclick="filterSelection(\'all\')"> Show All</button>\n'
		else:
			s += '  <button class="btn" onclick="filterSelection(\'' + k + '\')"> ' + v + '</button>\n'
	s += '</div>\n'
	return s

